<script setup>
  import {ref,onMounted} from 'vue'
  // 响应式状态
  const count = ref(0)

  // 用来修改状态，触发更新的函数
  function increment(){
    count.value++
  }

  onMounted(()=>{
    console.log(`The initial count is ${count.value}`)
  })

</script>

